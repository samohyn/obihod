type AnyBlock = { blockType: string; [k: string]: unknown }

async function main() {
  const payload = await getPayload({ config })

  const doc = await payload.findByID({ collection: 'services', id: PILLAR_ID, depth: 0 })
  if (!doc || doc.slug !== PILLAR_SLUG) {
    throw new Error(`Service id=${PILLAR_ID} не найден или slug != ${PILLAR_SLUG}`)
  }

  const current = (Array.isArray(doc.blocks) ? doc.blocks : []) as AnyBlock[]
  const byType = (t: string) => current.find((b) => b.blockType === t)

  // Reused sustained blocks (master-template T2_PILLAR порядок выводит resolver).
  const hero = byType('hero')
  const breadcrumbs = byType('breadcrumbs')
  const tldr = byType('tldr')
  const servicesGrid = byType('services-grid')
  const miniCase = byType('mini-case')
  const faq = byType('faq')
  const relatedServices = byType('related-services')
  const leadForm = byType('lead-form')
  const ctaBanner = byType('cta-banner') // первый — resolver dedup'ит

  // text-content — фикс markdown-бага: body как чистый markdown-string.
  const textContentSrc = byType('text-content') as AnyBlock | undefined
  const textContent = textContentSrc
    ? { ...textContentSrc, body: TEXT_CONTENT_BODY_MD }
    : { blockType: 'text-content', enabled: true, body: TEXT_CONTENT_BODY_MD }

  const newBlocks: AnyBlock[] = []
  const push = (b: AnyBlock | undefined) => {
    if (b) newBlocks.push(b)
  }
  // Кладём в master-template-логичном порядке (resolver всё равно reorder'ит).
  push(hero)
  push(breadcrumbs)
  push(tldr)
  push(textContent)
  push(servicesGrid)
  newBlocks.push(PRICING_BLOCK)
  newBlocks.push(CALCULATOR_BLOCK)
  newBlocks.push(PROCESS_BLOCK)
  push(miniCase)
  push(faq)
  push(ctaBanner)
  push(relatedServices)
  push(leadForm)

  await payload.update({
    collection: 'services',
    id: PILLAR_ID,
    data: { blocks: newBlocks as never, useTemplateV2: true },
  })

  console.log(
    `[fill-vyvoz-musora-content] updated Service id=${PILLAR_ID}: ${newBlocks.length} blocks, useTemplateV2=true`,
  )
  console.log(`  blocks: ${newBlocks.map((b) => b.blockType).join(', ')}`)
  process.exit(0)
}

main().catch((e) => {
  console.error('[fill-vyvoz-musora-content] FAILED:', e)
  process.exit(1)
})
